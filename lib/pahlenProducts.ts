/**
 * Apply standard 25% retail markup to a Pahlén base price.
 * Always use this when displaying Pahlén prices on the site.
 */
export function pahlenPrice(base: number | null): number | null {
  if (base === null) return null;
  return Math.round(base * 1.25);
}

